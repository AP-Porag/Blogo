     */
    public function store(Request $request)
    {
        //validation
        $this->validate($request,[
            'name'=>'required|unique:tags,name|min:3'
        ]);

        //insert data
        $tag = Tag::create([
            'name'=>$request->name,
            'slug'=>Str::slug($request->name,'-')
        ]);
        return response($tag);
    }

